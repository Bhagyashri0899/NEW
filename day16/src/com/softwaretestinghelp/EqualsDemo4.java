//equals() method in numeric data types

package com.softwaretestinghelp;

public class EqualsDemo4 {

	public static void main(String[] args) {
		
		    Long lng1 = 15l;
	        Long lng2 = 15l;    
	        Double dbl1 = 15.5d;
	        Double dbl2 = 20.0d;
	         
	        System.out.println("lng1 and lng2 are equal : "+(lng1.equals(lng2)));  
	        System.out.println("dbl1 and dbl2 are equal : "+(dbl1.equals(dbl2)));
	}

}
